package grammar_sample;

public class _200105_switch {

	public static void main(String[] args) {

		int value = 2;

		switch (value) {		
		case 1:
			System.out.println(value);
			break;
		case 2:
			System.out.println(value);
			break;
			
		default:
			System.out.println(value);
			break;
			
		}

	}

}
